import tkinter as tk
from gui import OrderApp


def main():
    """
    Главная функция приложения для управления заказами.
    
    Инициализирует и запускает графическое приложение Tkinter.
    Создает главное окно, инициализирует основной класс приложения
    и запускает главный цикл обработки событий.
    
    Notes
    -----
    Функция является точкой входа в приложение и должна вызываться
    только при непосредственном запуске скрипта.
    
    Examples
    --------
    >>> if __name__ == "__main__":
    ...     main()
    """
    # Создание главного окна Tkinter
    root = tk.Tk()
    
    # Создание экземпляра основного приложения
    app = OrderApp(root)
    
    # Запуск главного цикла обработки событий Tkinter
    # Программа будет работать до закрытия окна
    root.mainloop()


# Точка входа в приложение
if __name__ == "__main__":
    main()